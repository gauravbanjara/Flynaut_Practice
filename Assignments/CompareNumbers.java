public class CompareNumbers {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 2;
        if (num1 > num2) System.out.println("First number is greater.");
        else if (num1 < num2) System.out.println("Second number is greater.");
        else System.out.println("Both numbers are equal.");
    }
}


